struct debugger {
    typedef string::iterator si;

    static void call(si it, si ed) {}

    template <typename T, typename... aT>
    static void call(si it, si ed, T a, aT... rest) {
        string b;
        for (; *it != ','; ++it)
            if (*it != ' ') b += *it;
        cout << b << "=" << a << " ";
        call(++it, ed, rest...);
    }
};

void solve() {
    int n;
    cin >> n;

    vector<pair<double, double>> a(n);

    for (int i = 0; i < n; i++) {
        double x, y;
        cin >> x >> y;

        a[i] = {x, y};
    }

    vector<double> b(n, INT_MAX);
    vector<int> c;

    vector<double> d(n, INT_MAX);
    vector<int> e;

    vector<pair<double, double>> f;

    for (int i = 0; i < n; i++) {
        int ind = lower_bound(d.begin(), d.end(), a[i].first) - d.begin();
        e.push_back(ind);
        f.push_back({a[ind]});
        d[ind] = a[i].first;
    }

    for (int i = f.size() - 1; i >= 0; i--) {
        int ind = lower_bound(b.begin(), b.end(), f[i].second) - b.begin();
        c.push_back(ind);
        b[ind] = f[i].second;
    }
    sortall(c);

    cout << c[c.size() - 1] + 1 << "\n";
}

